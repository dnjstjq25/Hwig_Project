package com.hwig.admin.event;

public class EventVO {
	private int event_id;
	private String event_subject;
	private String event_content;
	private String event_content_img;
	private String event_banner_img;
	private String event_square_img;
	private String event_list_img;

	public int getEvent_id() {
		return event_id;
	}

	public void setEvent_id(int event_id) {
		this.event_id = event_id;
	}

	public String getEvent_subject() {
		return event_subject;
	}

	public void setEvent_subject(String event_subject) {
		this.event_subject = event_subject;
	}

	public String getEvent_content() {
		return event_content;
	}

	public void setEvent_content(String event_content) {
		this.event_content = event_content;
	}

	public String getEvent_content_img() {
		return event_content_img;
	}

	public void setEvent_content_img(String event_content_img) {
		this.event_content_img = event_content_img;
	}

	public String getEvent_banner_img() {
		return event_banner_img;
	}

	public void setEvent_banner_img(String event_banner_img) {
		this.event_banner_img = event_banner_img;
	}

	public String getEvent_square_img() {
		return event_square_img;
	}

	public void setEvent_square_img(String event_square_img) {
		this.event_square_img = event_square_img;
	}

	public String getEvent_list_img() {
		return event_list_img;
	}

	public void setEvent_list_img(String event_list_img) {
		this.event_list_img = event_list_img;
	}
}
